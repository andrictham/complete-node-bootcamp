const Tour = require('./tourModel');

exports.checkBody = (req, res, next) => {
  const { body } = req;
  if (!body.name || !body.price) {
    return res.status(400).json({
      status: 'fail',
      message: 'Missing name or price',
    });
  }
  next();
};

